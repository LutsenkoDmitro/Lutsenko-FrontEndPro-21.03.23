// Запитати у користувача довжину масиву та елементи
let n = prompt("Введіть довжину масиву:");
let arr = [];
for (let i = 0; i < n; i++) {
    let elem = prompt(`Введіть ${i+1}-й елемент:`);
    arr.push(elem);
}

// Відсортувати масив за зростанням
arr.sort(function(a, b){return a-b});
alert("Масив після сортування: " + arr);

// Видалити елементи з масиву з 2 по 4
arr.splice(1, 4);
alert("Масив після видалення елементів з 2 по 4: " + arr);